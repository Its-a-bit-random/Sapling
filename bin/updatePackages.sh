#!/usr/bin/env bash
set -euo pipefail

README="README.md"
PACKAGES_DIR="packages"
BUILD_SCRIPT="bin/build.sh"

if [ ! -f "$README" ]; then
  echo "README.md not found"
  exit 1
fi

if [ ! -d "$PACKAGES_DIR" ]; then
  echo "packages/ directory not found"
  exit 1
fi

mkdir -p "$(dirname "$BUILD_SCRIPT")"

to_pascal_case() {
  echo "$1" | awk -F '[-_ ]+' '{
    out=""
    for (i=1; i<=NF; i++) {
      if ($i != "") {
        part=tolower($i)
        out = out toupper(substr(part,1,1)) substr(part,2)
      }
    }
    print out
  }'
}

to_camel_case() {
  echo "$1" | awk -F '[-_ ]+' '{
    out=""
    for (i=1; i<=NF; i++) {
      if ($i != "") {
        part=tolower($i)
        if (i == 1) {
          out = out part
        } else {
          out = out toupper(substr(part,1,1)) substr(part,2)
        }
      }
    }
    print out
  }'
}

TMP_README="$(mktemp)"
TMP_SECTION="$(mktemp)"
TMP_PACKAGES="$(mktemp)"

# Collect package folders sorted
for dir in "$PACKAGES_DIR"/*; do
  [ -d "$dir" ] || continue
  basename "$dir"
done | sort > "$TMP_PACKAGES"

# ----------------------------
# Build Packages section
# ----------------------------
{
  echo "## Packages"
  echo
  echo "*This section is generated by [updatePackages.sh](https://github.com/Its-a-bit-random/Sapling/blob/main/bin/updatePackages.sh)*"
  echo
  echo "| Package | Links |"
  echo "| ------- | ----- |"

  while IFS= read -r folder_name; do
    [ -z "$folder_name" ] && continue

    package_name="$(to_pascal_case "$folder_name")"
    package_path="$(to_camel_case "$folder_name")"

    docs_url="https://sapling.itsabitrandom.com/api/$package_name"
    changelog_url="https://github.com/Its-a-bit-random/Sapling/blob/main/packages/$folder_name/CHANGELOG.md"
    pesde_url="https://pesde.dev/packages/sapling/$folder_name/"

    pesde='[![Pesde](.moonwave/static/Community-Package-link-pesde.svg)]'
    changelog='[![Changelog](.moonwave/static/Roblox-Styled-Original-link-changelog.svg)]'
    docs='[![Docs](.moonwave/static/Roblox-Styled-Original-link-docs.svg)]'

    echo "| $package_name | $pesde($pesde_url) $changelog($changelog_url) $docs($docs_url) |"
  done < "$TMP_PACKAGES"
} > "$TMP_SECTION"

# ----------------------------
# Replace section in README
# ----------------------------
awk -v section_file="$TMP_SECTION" '
BEGIN {
  in_packages = 0
  replaced = 0
}

/^## Packages$/ {
  while ((getline line < section_file) > 0) print line
  close(section_file)
  in_packages = 1
  replaced = 1
  next
}

/^## / && in_packages {
  in_packages = 0
}

!in_packages {
  print
}

END {
  if (!replaced) {
    print ""
    while ((getline line < section_file) > 0) print line
    close(section_file)
  }
}
' "$README" > "$TMP_README"

mv "$TMP_README" "$README"

rm -f "$TMP_SECTION" "$TMP_PACKAGES"
